fun main(args: Array<String>) {
// program lists and names variables
    var myByte: Byte = 2
    var myShort: Short = 4
    var myInt: Int = 8
    var myLong: Long = 999999L
    var myFloat: Float = 44.44F
    var myDouble: Double = 323.454
    var myBoolean: Boolean = true
    var myChar: Char = 'A'

    println("Byte: $myByte")
    println("Short: $myShort")
    println("Int: $myInt")
    println("Long: $myLong")
    println("Float: $myFloat")
    println("Double: $myDouble")
    println("Boolean: $myBoolean")
    println("Char: $myChar")
}